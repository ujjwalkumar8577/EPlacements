package com.ujjwalkumar.eplacements.utilities;

public class Triplet {
    public String first, second, third;

    public Triplet(String first, String second, String third) {
        this.first = first;
        this.second = second;
        this.third = third;
    }

    public Triplet() {
        this.first = "";
        this.second = "";
        this.third = "";
    }
}
